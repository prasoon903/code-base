DROP TABLE IF EXISTS #AllData
SELECT T1.*,
CASE 
	WHEN T2.Status = 1 THEN 'Account is in CAP Program'
	WHEN T2.Status = 2 THEN 'ChargedOff'
	WHEN T2.Status = 3 THEN 'Account is not eligible for contractual CO'
	WHEN T2.Status = 4 THEN 'Account status changed in current cycle'
	WHEN T2.Status = 5 THEN 'Account delinquency changed in current cycle'
END Reason
INTO #AllData
FROM #AllAccounts T1
JOIN #TempData T2 ON (T1.AccountUUID = T2.AccountUUID) 
WHERE T2.Status > 0




DROP TABLE IF EXISTS #AllAccounts
SELECT T1.*, B.acctID, AccountNumber, CycleDueDTD, SystemStatus, ChargeOffDateParam, ccinhparent125AID, ManualInitialChargeOffReason, A.StatusDescription ManualStatus, A.StatusDescription SystemStatusDesc
INTO #AllAccounts
FROM #TempData T1
JOIN BSegment_Primary B WITH (NOLOCK) ON (T1.AccountUUID = B.UniversalUniqueID)
JOIN BSegmentCreditCard c WITH (NOLOCK) ON (B.acctID = C.acctID)
LEFT JOIN Astatusaccounts A WITH (NOLOCK) ON (B.ccinhparent125AID = A.parent01AID AND A.MerchantAID = B.parent05AID)
LEFT JOIN Astatusaccounts A1 WITH (NOLOCK) ON (B.SystemStatus = A1.parent01AID AND A1.MerchantAID = B.parent05AID)

